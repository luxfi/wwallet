<template>
    <modal ref="modal" :title="$t('modal.ledger_upgrade.title')" @beforeClose="beforeClose">
        <div class="ledger_block">
            <ol>
                <li>Connect the ledger device to your computer.</li>
                <li>Enter your PIN and access your device.</li>
                <li>
                    Ensure you have installed the
                    <b>Lux App v{{ minV }}</b>
                    or above and open it on your device.
                </li>
            </ol>
            <p style="margin-top: 12px !important">
                <small>
                    If you do not have the Lux app on your ledger, please add it through the
                    <a href="https://www.ledger.com/ledger-live/download" target="_blank">
                        Ledger Live
                    </a>
                    app manager. The minimum version required to use the app is version {{ minV }},
                    more instructions can be found
                    <a
                        target="_blank"
                        href="https://support.lux.network/en/articles/6150237-how-to-use-a-ledger-nano-s-or-nano-x-with-luxnet"
                    >
                        here
                    </a>
                    .
                </small>
            </p>
        </div>
    </modal>
</template>
<script lang="ts">
import { WalletType } from '@/js/wallets/types'

import 'reflect-metadata'
import { Vue, Component, Watch } from 'vue-property-decorator'

import Modal from './Modal.vue'
import { MIN_LEDGER_V } from '@/js/wallets/constants'

@Component({
    components: {
        Modal,
    },
})
class LedgerUpgrade extends Vue {
    $refs!: {
        modal: Modal
    }

    open() {
        this.$refs.modal.open()
    }

    close() {
        this.$refs.modal.close()
    }

    beforeClose() {
        this.$store.commit('Ledger/setIsUpgradeRequired', false)
    }

    destroyed() {
        this.$store.commit('Ledger/setIsUpgradeRequired', false)
    }

    get minV() {
        return MIN_LEDGER_V
    }

    get isActive() {
        return this.$store.state.Ledger.isUpgradeRequired
    }

    get wallet() {
        return this.$store.state.activeWallet as WalletType
    }

    @Watch('isActive', { immediate: true })
    onActive(val: boolean): void {
        if (!this.$refs.modal) return
        if (val) {
            this.open()
        } else {
            this.close()
        }
    }
}
export default LedgerUpgrade
</script>
<style scoped lang="scss">
